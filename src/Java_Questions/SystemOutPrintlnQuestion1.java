package Java_Questions;

public class SystemOutPrintlnQuestion1 {
    public static void main(String[] args) {
        //+기호가 숫자를 문자로 인식.
        System.out.println("2 + 5 = " + 2 + 5);     //25
        //()를 인식을 하고 +기호가 수식을 하고 문자로 인식.
        System.out.println("2 + 5 = " + (2 + 5));   //7
    }
}
